#import "WDSShareDreamViewController.h"
#import "WDSAddDreamViewController.h"
#import "JASidePanelController.h"
#import "UIViewController+JASidePanel.h"

@interface WDSShareDreamViewController ()

@end

@implementation WDSShareDreamViewController

#pragma mark - Managing the detail item

- (void)setDetailItem:(id)newDetailItem
{
    if (_detailItem != newDetailItem) {
        _detailItem = newDetailItem;
        
        // Update the view.
        [self configureView];
    }
}

- (void)configureView
{
    // Update the user interface for the detail item.
    
    if (self.detailItem) {
        self.nameLabel.text = [self.detailItem title];
        self.descriptionLabel.text = [self.detailItem description];
        
    }
}


- (id)initWithNibName:(NSString *)nibNameOrNil bundle:(NSBundle *)nibBundleOrNil
{
    self = [super initWithNibName:nibNameOrNil bundle:nibBundleOrNil];
    if (self) {
        // Custom initialization
    }
    return self;
}

- (void)viewDidLoad
{
    [super viewDidLoad];
    // Do any additional setup after loading the view.
    [self configureView];
    
}

- (void)didReceiveMemoryWarning
{
    [super didReceiveMemoryWarning];
    // Dispose of any resources that can be recreated.
}

- (IBAction)shareDream:(id)sender
{
    NSLog(@"share");
}

#pragma mark - Navigation

- (void)prepareForSegue:(UIStoryboardSegue *)segue sender:(id)sender
{
    if ([[segue identifier] isEqualToString:@"showEditDetail"])
    {
        [[segue destinationViewController] setDetailItem:_detailItem];
    }
}

@end
